
public class Conversor {

	public Object converteZeroAdez(int i) {
		if (i == 0){
			return "zero";
		}
		if (i == 1){
			return "um";
		}
		else if (i == 2){
			return "dois";
		}
		else if (i == 3){
			return "tres";
		}
		else if (i == 4){
			return "quatro";
		}
		else if (i == 5){
			return "cinco";
		}
		else if (i == 6){
			return "seis";
		}
		else if (i == 7){
			return "sete";
		}
		else if (i == 8){
			return "oito";
		}
		else if (i == 9){
			return "nove";
		}
		else if (i == 10){
			return "dez";
		}
		return null;
	}

}
